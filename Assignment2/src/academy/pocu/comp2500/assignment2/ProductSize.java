package academy.pocu.comp2500.assignment2;

public class ProductSize {
    private int width;
    private int height;

    public ProductSize(int width, int height) {
        this.width = width;
        this.height = height;
    }
    public int getWidth() {
        return this.width;
    }
    public int getHeight() {
        return this.height;
    }
}
